from pyrogram import Client, filters
from pyrogram.types import InlineKeyboardMarkup, InlineKeyboardButton, Message
from pyrogram.errors import ChatAdminRequired, UserNotParticipant, ChatWriteForbidden
from ANNIEMUSIC import app

#--------------------------
# Change this to your private channel ID (starts with -100)
MUST_JOIN = "-100123456789"  # Replace with your actual private channel ID
#------------------------

@app.on_message(filters.incoming & filters.private, group=-1)
async def must_join_channel(app: Client, msg: Message):
    if not MUST_JOIN:
        return
    try:
        try:
            await app.get_chat_member(MUST_JOIN, msg.from_user.id)
        except UserNotParticipant:
            # For private channels, we always need to get the invite link
            try:
                chat_info = await app.get_chat(MUST_JOIN)
                # Check if we have a valid invite link
                if not chat_info.invite_link:
                    # Create an invite link if one doesn't exist and bot has permission
                    link = await app.export_chat_invite_link(MUST_JOIN)
                else:
                    link = chat_info.invite_link
                
                await msg.reply_photo(
                    photo="https://telegra.ph/file/51600957eaaf96ad308c7.jpg", 
                    caption=f"๏ ᴀᴄᴄᴏʀᴅɪɴɢ ᴛᴏ ᴍʏ ᴅᴀᴛᴀʙᴀsᴇ ʏᴏᴜ'ᴠᴇ ɴᴏᴛ ᴊᴏɪɴᴇᴅ [๏sᴜᴘᴘᴏʀᴛ๏]({link}) ʏᴇᴛ, ɪғ ʏᴏᴜ ᴡᴀɴᴛ ᴛᴏ ᴜsᴇ ᴍᴇ ᴛʜᴇɴ ᴊᴏɪɴ [๏sᴜᴘᴘᴏʀᴛ๏]({link}) ᴀɴᴅ sᴛᴀʀᴛ ᴍᴇ ᴀɢᴀɪɴ ! ",
                    reply_markup=InlineKeyboardMarkup(
                        [
                            [
                                InlineKeyboardButton("๏Jᴏɪɴ๏", url=link),
                            ]
                        ]
                    )
                )
                await msg.stop_propagation()
            except ChatWriteForbidden:
                pass
            except Exception as e:
                print(f"Error creating invite link: {e}")
                # Fallback message if we can't create an invite link
                await msg.reply_text(
                    "I couldn't generate an invite link. Make sure I have the right permissions in the channel."
                )
    except ChatAdminRequired:
        print(f"๏ᴘʀᴏᴍᴏᴛᴇ ᴍᴇ ᴀs ᴀɴ ᴀᴅᴍɪɴ ɪɴ ᴛʜᴇ ᴍᴜsᴛ_Jᴏɪɴ ᴄʜᴀᴛ ๏: {MUST_JOIN} !")